char my_next_char(char c)
{
  if ( (c >= 65 && c <= 90) || (c >= 97 && c <= 122) )
  {
    c = ( c == 90 || c == 122 ? c - 25 : c + 1 );
  }
  else
  {
    c = 32;
  }
  return (c);
}
